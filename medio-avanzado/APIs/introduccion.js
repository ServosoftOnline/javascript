/*
	📌 Que son las API
	Application Programming Interface
		- Forma que tengo para poder hablarle a algun servicio externo
		- La información que pasaremos seran objetos JSON
*/

/*
	📌 APIs del navegador web
	Los navegadores nos dan acceso a diferentes API con las que podemos:

	- Local Storage API 
		- Trabajar con almacenamiento local.
		- Permite guardar información de forma local en el navegador que no se perderá si el usuario refresca la pagina o la cierra.
			- 5MB maximo. Suele almacenar configuracion del usuario, algunos tokens de inicio de sesion, ... 
		- Si considero que perder la información cuando cierre el navegador no es importante podré usar Context API de react.

	- API para trabajar con fechas llamada date		
		- Mas información y en el enlace: https://developer.mozilla.org/es/docs/Web/JavaScript/Reference/Global_Objects/Date

	- Fetch API - Permite hacer peticiones a servidores externos y que ellos nos devuelva información
	- History API - Para poder acceder al historial de la página. Permite ir hacia delante, hacia atras, cambiar la barra de direcciones
	- Validation API - Para hacer uso de las funciones del navegador para validar formularios. Por ahora valido yo.
	- Más
*/

